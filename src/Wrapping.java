
public class Wrapping {

	public static void main(String[] args) {
		Integer ni = 10; //in-boxing
		int mi = ni; //out-boxing
		System.out.println(ni + "=" + mi);
		
		Short ns = (short)20; //in-boxing
		short ms = ns; //out-boxing
		System.out.println(ns + "=" + ms);

		Float nf =  3.2F; //in-boxing
		float mf = nf; //out-boxing
		System.out.println(nf + "=" + mf);

	
	}

}
